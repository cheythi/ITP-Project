    private void ctelephoneKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ctelephoneKeyPressed
        // TODO add your handling code here:
        
            
        String tp = ctelephone.getText();
        
        int length = tp.length();
        
        char c = evt.getKeyChar();
        
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
            
            if(length<10){
            
                ctelephone.setEditable(true);
            }
            else{
            
                ctelephone.setEditable(false);
                
                JOptionPane.showMessageDialog(null,"Invalidate phone number");
            }
        
        }
        
        else{
          if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
              ctelephone.setEditable(true);
          }
          else{
              ctelephone.setEditable(false);
          }
        }

    }//GEN-LAST:event_ctelephoneKeyPressed

