    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        
        
        if( cname.getText().equals(" ") ||  caddress.getText().equals(" ") || ctelephone.getText().equals(" ") ||  em.getText().equals( ""))
 {
     JOptionPane.showMessageDialog(rootPane, "Required fields cannot be empty", "Warning", JOptionPane.WARNING_MESSAGE);
 }
 
            
 

 else{     
            
            
        //String ecid = cid.getText();
        String ename = cname.getText();
        String eaddress=caddress.getText();
        String etp = ctelephone.getText();
        String email = em.getText();
       
        
        
       // PreparedStatement pst;
        
        try{
                String q = "INSERT INTO company (companyname,address,telephone_number,email) values (?,?,?,?) ";
        
        pst = (PreparedStatement) con.prepareStatement(q);
      
    
        pst.setString(1, ename);
        pst.setString(2, eaddress);
        pst.setString(3, etp);
        pst.setString(4, email);
       
            
        
        pst.executeUpdate();
        
        tableload();
        
        }
        catch(SQLException e){
            JOptionPane.showConfirmDialog(this,e);
        }
        
        cid.setText("");
        cname.setText("");
        caddress.setText("");
        ctelephone.setText("");
        em.setText("");
        
              }
    
        
        
    }//GEN-LAST:event_addActionPerformed

